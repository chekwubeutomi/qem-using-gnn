def feature_normalize(dataset: QuantumCircuitDataset) -> tuple[np.ndarray, np.ndarray]:
    """
    Compute per-feature mean and std over the training set for normalisation.
    Returns (mean, std) arrays of shape [node_feat_dim].
    """
    all_feats = []
    for data in dataset:
        all_feats.append(data.x.numpy())
    all_feats = np.vstack(all_feats)
    mean = all_feats.mean(axis=0)
    std  = all_feats.std(axis=0) + 1e-8
    return mean, std


def normalize_dataset(
    dataset : QuantumCircuitDataset,
    mean    : np.ndarray,
    std     : np.ndarray,
) -> None:
    """Apply z-score normalization in-place."""
    mean_t = torch.tensor(mean, dtype=torch.float)
    std_t  = torch.tensor(std,  dtype=torch.float)
    for data in dataset:
        data.x = (data.x - mean_t) / std_t


def train_one_epoch(
    model      : nn.Module,
    loader     : DataLoader,
    optimizer  : torch.optim.Optimizer,
    device     : torch.device,
) -> float:
    model.train()
    total_loss = 0.0
    total_samples = 0

    for batch in loader:
        batch = batch.to(device)
        optimizer.zero_grad()

        pred = model(batch)
        loss = F.mse_loss(pred, batch.y)

        loss.backward()
        # Gradient clipping for stability
        torch.nn.utils.clip_grad_norm_(model.parameters(), max_norm=1.0)
        optimizer.step()

        total_loss    += loss.item() * batch.num_graphs
        total_samples += batch.num_graphs

    return total_loss / total_samples


@torch.no_grad()
def evaluate(
    model  : nn.Module,
    loader : DataLoader,
    device : torch.device,
) -> dict:
    model.eval()
    preds, targets, noisy_vals = [], [], []

    for batch in loader:
        batch = batch.to(device)
        pred  = model(batch)
        preds.append(pred.cpu())
        targets.append(batch.y.cpu())
        noisy_vals.append(batch.noisy_val.cpu())

    preds      = torch.cat(preds).numpy().flatten()
    targets    = torch.cat(targets).numpy().flatten()
    noisy_vals = torch.cat(noisy_vals).numpy().flatten()

    mse_mitigated = float(np.mean((preds - targets) ** 2))
    mse_noisy     = float(np.mean((noisy_vals - targets) ** 2))
    mae_mitigated = float(np.mean(np.abs(preds - targets)))
    mae_noisy     = float(np.mean(np.abs(noisy_vals - targets)))

    # R² score
    ss_res = np.sum((preds - targets) ** 2)
    ss_tot = np.sum((targets - targets.mean()) ** 2) + 1e-12
    r2     = 1.0 - ss_res / ss_tot

    # Mitigation ratio: how much of the noise error was corrected
    noise_error     = mse_noisy
    remaining_error = mse_mitigated
    mitigation_ratio = 1.0 - remaining_error / (noise_error + 1e-12)

    # return {
    #     "mse"              : mse_mitigated,
    #     "mae"              : mae_mitigated,
    #     "mse_noisy"        : mse_noisy,
    #     "mae_noisy"        : mae_noisy,
    #     "r2"               : r2,
    #     "mitigation_ratio" : mitigation_ratio,
    # }

    return {
        "mse" : mse_mitigated,
        "mae" : mae_mitigated,
        "mse_noisy" : mse_noisy,
        "mae_noisy" : mae_noisy,
        "r2" : r2,
        "mitigation_ratio" : mitigation_ratio,
        # ADD THESE THREE LINES BELOW:
        "_preds": preds,      
        "_targets": targets,  
        "_noisy": noisy_vals  
    }


