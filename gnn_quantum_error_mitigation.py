def parse_args():
    p = argparse.ArgumentParser(
        description="GNN Quantum Error Mitigation",
        formatter_class=argparse.ArgumentDefaultsHelpFormatter,
    )
    p.add_argument(
        "--dataset",
        type=str, default="quantum_dataset.json",
        help="Path to JSON dataset produced by generate_quantum_dataset.py"
    )
    p.add_argument(
        "--mode",
        choices=["train", "eval"],
        default="train",
        help="'train' to fit the model; 'eval' to run inference with a saved checkpoint"
    )
    p.add_argument(
        "--checkpoint",
        type=str, default="best_model.pt",
        help="Path to save (train) or load (eval) the model checkpoint"
    )
    p.add_argument("--epochs",       type=int,   default=100)
    p.add_argument("--batch_size",   type=int,   default=32)
    p.add_argument("--lr",           type=float, default=1e-3)
    p.add_argument("--hidden_size",  type=int,   default=128)
    p.add_argument("--num_heads",    type=int,   default=4,
                   help="Number of attention heads per TransformerConv layer")
    p.add_argument("--dropout",      type=float, default=0.2)
    return p.parse_args()


if __name__ == "__main__":
    args = parse_args()

    if args.mode == "train":
        run_training(args)
    else:
        run_inference(args)
