"""
GNN-based Quantum Error Mitigation
====================================
Implements a Graph Neural Network (GNN) that learns to mitigate noise in
quantum circuit expectation values. Consumes graph-encoded circuit data
produced by generate_quantum_dataset.py.

Architecture (per the reference paper):
  - 2 × Transformer Convolution layers (multi-head attention on edges)
  - ASAPooling layers for graph-level readout
  - 2 × Dense layers (hidden_size=128)
  - Dropout regularisation throughout
  - Input: circuit DAG graph + noisy expectation values
  - Output: mitigated (near-ideal) expectation values

Usage
-----
# 1. Generate data (if you haven't already):
    python generate_quantum_dataset.py --num_circuits 200 --output dataset.json

# 2. Train the GNN:
    python gnn_quantum_error_mitigation.py --dataset dataset.json --epochs 100

# 3. Evaluate / run inference:
    python gnn_quantum_error_mitigation.py --dataset dataset.json --mode eval --checkpoint best_model.pt
"""

