def run_training(args):
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\n{'='*60}")
    print(f"  GNN Quantum Error Mitigation — Training")
    print(f"{'='*60}")
    print(f"  Device  : {device}")
    print(f"  Dataset : {args.dataset}")
    print(f"  Epochs  : {args.epochs}")
    print(f"  Batch   : {args.batch_size}")
    print(f"  LR      : {args.lr}")
    print(f"{'='*60}\n")

    # ---- Load data ----
    print("Loading dataset ...")
    with open(args.dataset) as f:
        raw = json.load(f)

    # Split at the circuit level to avoid data leakage across observables
    train_raw, test_raw = train_test_split(raw, test_size=0.15, random_state=42)
    train_raw, val_raw  = train_test_split(train_raw, test_size=0.15, random_state=42)

    print(f"  Circuits  — train: {len(train_raw)}  val: {len(val_raw)}  test: {len(test_raw)}")

    train_ds = QuantumCircuitDataset(train_raw)
    val_ds   = QuantumCircuitDataset(val_raw)
    test_ds  = QuantumCircuitDataset(test_raw)

    # ---- Normalise node features ----
    print("Normalising features ...")
    mean, std = feature_normalize(train_ds)
    normalize_dataset(train_ds, mean, std)
    normalize_dataset(val_ds,   mean, std)
    normalize_dataset(test_ds,  mean, std)

    # Save normalisation stats alongside checkpoint
    norm_path = Path(args.checkpoint).with_suffix(".norm.npz")
    np.savez(norm_path, mean=mean, std=std)

    train_loader = DataLoader(train_ds, batch_size=args.batch_size, shuffle=True,  num_workers=0)
    val_loader   = DataLoader(val_ds,   batch_size=args.batch_size, shuffle=False, num_workers=0)
    test_loader  = DataLoader(test_ds,  batch_size=args.batch_size, shuffle=False, num_workers=0)

    # ---- Build model ----
    model = QuantumErrorMitigationGNN(
        node_feat_dim  = NODE_FEAT_DIM + 1,
        edge_feat_dim  = EDGE_FEAT_DIM,
        hidden_size    = args.hidden_size,
        num_heads      = args.num_heads,
        dropout        = args.dropout,
    ).to(device)

    n_params = sum(p.numel() for p in model.parameters() if p.requires_grad)
    print(f"\n  Model parameters: {n_params:,}\n")

    optimizer = Adam(model.parameters(), lr=args.lr, weight_decay=1e-5)
    scheduler = ReduceLROnPlateau(optimizer, mode="min", patience=10, factor=0.5)


    best_val_mse = math.inf
    history = {"train_mse": [], "val_mse": [], "val_r2": [], "val_mitigation_ratio": []}

    # ---- Training loop ----
    for epoch in range(1, args.epochs + 1):
        t0 = time.time()
        train_mse = train_one_epoch(model, train_loader, optimizer, device)
        val_metrics = evaluate(model, val_loader, device)

        scheduler.step(val_metrics["mse"])

        history["train_mse"].append(train_mse)
        history["val_mse"].append(val_metrics["mse"])
        history["val_r2"].append(val_metrics["r2"])
        history["val_mitigation_ratio"].append(val_metrics["mitigation_ratio"])

        # Save best model
        if val_metrics["mse"] < best_val_mse:
            best_val_mse = val_metrics["mse"]
            torch.save(
                {"model_state": model.state_dict(), "args": vars(args)},
                args.checkpoint,
            )
            flag = " ← best"
        else:
            flag = ""

        elapsed = time.time() - t0
        print(
            f"  Epoch {epoch:>4}/{args.epochs}"
            f"  train_MSE={train_mse:.5f}"
            f"  val_MSE={val_metrics['mse']:.5f}"
            f"  val_R²={val_metrics['r2']:.4f}"
            f"  mitigation={val_metrics['mitigation_ratio']*100:.1f}%"
            f"  ({elapsed:.1f}s){flag}"
        )

    # ---- Final test evaluation ----
    print(f"\n{'='*60}")
    print("  Final Test Evaluation (best checkpoint)")
    print(f"{'='*60}")
    ckpt = torch.load(args.checkpoint, map_location=device)
    model.load_state_dict(ckpt["model_state"])
    test_metrics = evaluate(model, test_loader, device)

    improvement_factor = test_metrics['mse_noisy'] / (test_metrics['mse'] + 1e-12)

    print(f"  MSE  (mitigated) : {test_metrics['mse']:.6f}")
    print(f"  MSE  (noisy)     : {test_metrics['mse_noisy']:.6f}")
    print(f"  MAE  (mitigated) : {test_metrics['mae']:.6f}")
    print(f"  MAE  (noisy)     : {test_metrics['mae_noisy']:.6f}")
    print(f"  R²               : {test_metrics['r2']:.4f}")
    print(f"  Mitigation ratio : {test_metrics['mitigation_ratio']*100:.2f}%")
    print(f"  Improvement factor : {improvement_factor:.2f}x")

    # Save training history
    history_path = Path(args.checkpoint).with_suffix(".history.json")
    with open(history_path, "w") as f:
        json.dump(history, f, indent=2)
    print(f"\n  History saved → {history_path}")
    print(f"  Best model  saved → {args.checkpoint}")
    print(f"  Norm stats  saved → {norm_path}")


def run_inference(args):
    """Load a trained model and run inference on a dataset."""
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"\n{'='*60}")
    print("  GNN Quantum Error Mitigation — Inference")
    print(f"{'='*60}")

    with open(args.dataset) as f:
        raw = json.load(f)

    ds = QuantumCircuitDataset(raw)

    # Load normalisation stats
    norm_path = Path(args.checkpoint).with_suffix(".norm.npz")
    if norm_path.exists():
        npz  = np.load(norm_path)
        mean, std = npz["mean"], npz["std"]
        normalize_dataset(ds, mean, std)
        print(f"  Applied normalisation from {norm_path}")
    else:
        print("  Warning: no normalisation file found — features not normalised")

    loader = DataLoader(ds, batch_size=args.batch_size, shuffle=False, num_workers=0)

    ckpt  = torch.load(args.checkpoint, map_location=device)
    saved = ckpt.get("args", {})

    model = QuantumErrorMitigationGNN(
        node_feat_dim = NODE_FEAT_DIM + 1,
        edge_feat_dim = EDGE_FEAT_DIM,
        hidden_size   = saved.get("hidden_size", 128),
        num_heads     = saved.get("num_heads",   4),
        dropout       = 0.0,         # no dropout at inference
    ).to(device)
    model.load_state_dict(ckpt["model_state"])

    metrics = evaluate(model, loader, device)
    print(f"\n  MSE  (mitigated) : {metrics['mse']:.6f}")
    print(f"  MSE  (noisy)     : {metrics['mse_noisy']:.6f}")
    print(f"  R²               : {metrics['r2']:.4f}")
    print(f"  Mitigation ratio : {metrics['mitigation_ratio']*100:.2f}%")


