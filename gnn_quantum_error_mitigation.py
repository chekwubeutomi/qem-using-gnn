# 2.  GNN Model
# ---------------------------------------------------------------------------

class QuantumErrorMitigationGNN(nn.Module):
    """
    GNN for quantum error mitigation.

    Architecture:
        TransformerConv (layer 1)  →  ASAPooling (partial)
        TransformerConv (layer 2)  →  ASAPooling (full graph readout)
        Dense layer 1 (128)
        Dense layer 2 (128)
        Output head (1 per observable, i.e. 1 here)

    The noisy expectation value is concatenated to node features before the
    GNN layers, so the model can learn the correction Δ = ideal - noisy.
    """

    def __init__(
        self,
        node_feat_dim   : int   = NODE_FEAT_DIM + 1,   # +1 for noisy value
        edge_feat_dim   : int   = EDGE_FEAT_DIM,
        hidden_size     : int   = 128,
        num_heads       : int   = 4,
        dropout         : float = 0.2,
        add_self_loops  : bool  = True,
    ):
        super().__init__()

        self.dropout_rate = dropout

        # ---- Input projection ----
        self.input_proj = Linear(node_feat_dim, hidden_size)

        # ---- Transformer Convolution Layer 1 ----
        self.conv1 = TransformerConv(
            in_channels  = hidden_size,
            out_channels = hidden_size // num_heads,
            heads        = num_heads,
            edge_dim     = edge_feat_dim,
            dropout      = dropout,
            concat       = True,
            beta         = True,          # use key/query/value beta (more expressive)
            root_weight  = True,
        )
        self.bn1 = nn.BatchNorm1d(hidden_size)

        # ---- SAGPooling after layer 1 (down-samples graph) ----
        self.pool1 = SAGPooling(
            in_channels = hidden_size,
            ratio       = 0.8,            # keep 80% of nodes
            GNN         = TransformerConv,
        )

        # ---- Transformer Convolution Layer 2 ----
        self.conv2 = TransformerConv(
            in_channels  = hidden_size,
            out_channels = hidden_size // num_heads,
            heads        = num_heads,
            edge_dim     = edge_feat_dim,
            dropout      = dropout,
            concat       = True,
            beta         = True,
            root_weight  = True,
        )
        self.bn2 = nn.BatchNorm1d(hidden_size)

        # ---- SAGPooling after layer 2 ----
        self.pool2 = SAGPooling(
            in_channels = hidden_size,
            ratio       = 0.5,
            GNN         = TransformerConv,
        )

        # ---- Global pooling fallback (in case pool2 returns 0 nodes) ----
        # This also combines max and mean for richer graph representation.
        graph_repr_dim = hidden_size * 2 + 1   # max+mean pooled + noisy_val

        # ---- Dense layers ----
        self.fc1    = nn.Linear(graph_repr_dim, hidden_size)
        self.fc2    = nn.Linear(hidden_size,    hidden_size)
        self.output = nn.Linear(hidden_size,    1)

        self.dropout = nn.Dropout(p=dropout)

    def forward(self, data: Data) -> torch.Tensor:
        x, edge_index, edge_attr = data.x, data.edge_index, data.edge_attr
        batch     = data.batch
        noisy_val = data.noisy_val           # [B, 1]

        # Extract and validate edge attributes
        if edge_attr is None or edge_attr.size(0) == 0:
            edge_attr = torch.zeros(
                (edge_index.size(1), EDGE_FEAT_DIM),
                device=x.device
            )

        # ---- Input projection + activation ----
        x = F.elu(self.input_proj(x))
        x = self.dropout(x)

        # ---- TransformerConv Layer 1 ----
        x = self.conv1(x, edge_index, edge_attr)
        x = self.bn1(x)
        x = F.elu(x)
        x = self.dropout(x)

        # ---- SAGPooling 1 ----
        # SAGPooling returns (x, edge_index, edge_attr, batch, perm, score)
        if edge_index.size(1) > 0:
            x, edge_index, edge_attr, batch, _, _ = self.pool1(
                x, edge_index, edge_attr=edge_attr, batch=batch
            )
        # Ensure edge_attr matches edge_index after pooling
        if edge_attr is None or edge_attr.size(0) != edge_index.size(1):
            edge_attr = torch.zeros(
                (edge_index.size(1), EDGE_FEAT_DIM), device=x.device
            )

        # ---- TransformerConv Layer 2 ----
        x = self.conv2(x, edge_index, edge_attr)
        x = self.bn2(x)
        x = F.elu(x)
        x = self.dropout(x)

        # ---- Global readout (mean + max pooling → richer representation) ----
        x_mean = global_mean_pool(x, batch)      # [B, hidden]
        x_max  = global_max_pool(x, batch)       # [B, hidden]
        graph_repr = torch.cat([x_mean, x_max, noisy_val], dim=-1)   # [B, 2H+1]

        # ---- Dense layers ----
        out = F.elu(self.fc1(graph_repr))
        out = self.dropout(out)
        out = F.elu(self.fc2(out))
        out = self.dropout(out)
        out = self.output(out)                   # [B, 1]

        return out


