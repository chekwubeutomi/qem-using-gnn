# ---------------------------------------------------------------------------
# 1.  Dataset: parse JSON graph encoding → PyG Data objects
# ---------------------------------------------------------------------------

NODE_FEAT_DIM  = 22   # DAGOpNode feature dimension (from generate_quantum_dataset.py)
EDGE_FEAT_DIM  = 3    # wire features: T1, T2, readout_error


def _pad_or_truncate(feat: list, target_dim: int) -> list:
    """Ensure a feature vector is exactly target_dim elements."""
    if len(feat) >= target_dim:
        return feat[:target_dim]
    return feat + [0.0] * (target_dim - len(feat))


def sample_to_pyg(sample: dict, obs_idx: int) -> Data | None:
    """
    Convert one dataset sample (one observable) into a PyG Data object.

    The graph has only DAGOpNode nodes (gate nodes).  DAGInNode/DAGOutNode
    are boundary nodes with no meaningful features and are excluded.
    Edges are the op→op wires from the circuit DAG.

    Parameters
    ----------
    sample   : dict with keys circuit_graph, noisy_exp_values, ideal_exp_value
    obs_idx  : which observable index to use as the label

    Returns
    -------
    PyG Data or None if the circuit graph is degenerate.
    """
    graph = sample["circuit_graph"]
    op_feats = graph["nodes"]["DAGOpNode"]

    if not op_feats:
        return None

    # ---- Node features ----
    x = torch.tensor(
        [_pad_or_truncate(f, NODE_FEAT_DIM) for f in op_feats],
        dtype=torch.float
    )

    # ---- Edge index (op → op wires only) ----
    op_op_edges = graph["edges"].get("DAGOpNode_wire_DAGOpNode", {})
    src_list = op_op_edges.get("edge_index", [[], []])[0]
    dst_list = op_op_edges.get("edge_index", [[], []])[1]
    edge_attrs = op_op_edges.get("edge_attr", [])

    if src_list:
        edge_index = torch.tensor([src_list, dst_list], dtype=torch.long)
        edge_attr  = torch.tensor(
            [_pad_or_truncate(a, EDGE_FEAT_DIM) for a in edge_attrs],
            dtype=torch.float
        )
        # Make undirected so information can flow both ways
        edge_index, edge_attr = to_undirected(edge_index, edge_attr, reduce="mean")
    else:
        edge_index = torch.zeros((2, 0), dtype=torch.long)
        edge_attr  = torch.zeros((0, EDGE_FEAT_DIM), dtype=torch.float)

    # ---- Noisy expectation value (input signal) ----
    # noisy_exp_values is a list of trials, each a list of obs values
    noisy_trials = sample["noisy_exp_values"]   # shape: [n_trials, n_obs]
    noisy_val    = float(np.mean([t[obs_idx] for t in noisy_trials]))

    # ---- Ideal expectation value (label) ----
    ideal_val = float(sample["ideal_exp_value"][obs_idx])

    # ---- Global graph feature: noisy value appended to each node ----
    noisy_tensor = torch.full((x.size(0), 1), noisy_val, dtype=torch.float)
    x_aug = torch.cat([x, noisy_tensor], dim=1)   # [N, NODE_FEAT_DIM + 1]

    data = Data(
        x          = x_aug,
        edge_index = edge_index,
        edge_attr  = edge_attr,
        y          = torch.tensor([[ideal_val]], dtype=torch.float),
        noisy_val  = torch.tensor([[noisy_val]], dtype=torch.float),
    )
    return data


class QuantumCircuitDataset(Dataset):
    """
    PyG Dataset that converts the JSON dataset generated by
    generate_quantum_dataset.py into graph data objects.

    Each circuit × observable pair becomes one graph sample.
    """

    def __init__(self, samples: list[dict], transform=None):
        super().__init__(transform=transform)
        self._data_list: list[Data] = []

        for sample in samples:
            n_obs = len(sample["ideal_exp_value"])
            for obs_idx in range(n_obs):
                data = sample_to_pyg(sample, obs_idx)
                if data is not None:
                    self._data_list.append(data)

        print(f"  Loaded {len(self._data_list)} graph samples "
              f"from {len(samples)} circuits.")

    def len(self):
        return len(self._data_list)

    def get(self, idx):
        return self._data_list[idx]


